package practice.Task_Jul9th;

public class Integer_Wrapper {

    public static void main(String[] args) {
        int a = 50;
        System.out.println("Primite:"+a);
        Integer b = a;
        System.out.println("Wrapper:"+b);
        a = b;
        System.out.println("Unboxing:"+a);

    }



}
